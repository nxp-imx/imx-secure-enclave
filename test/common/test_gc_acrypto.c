	gc_acrypto_args.key_buff2 = pub_exp_buff;
	gc_acrypto_args.key_buff1_size = sizeof(modulus_buff);
	gc_acrypto_args.key_buff2_size = sizeof(pub_exp_buff);

	printf("\n------------------------------------------------\n");
	printf("Generic Crypto - Asymmetric Crypto Test:");
	printf("\n------------------------------------------------\n");

