	gc_acrypto_args.flags = 0;
	gc_acrypto_args.bit_key_sz = HSM_KEY_SIZE_RSA_2048;
	gc_acrypto_args.data_buff1 = data_buff1;
	gc_acrypto_args.data_buff2 = data_buff2;
	gc_acrypto_args.data_buff1_size = sizeof(data_buff1);
